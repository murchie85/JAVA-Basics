/// TUTORIAL 
// WHILE LOOP ALLOWS YOU TO ITERATE SEVERAL TIMES WITHOUT REPEATING THE CODE
// I.E. IT REPEATS WHILE A CONDITION IS TRUE

public class whileloop {
	public static void main(String args[]) {
		int counter = 0;
		while (counter < 10) {
			System.out.println(counter);
			counter++;
		}
		
	}
}
