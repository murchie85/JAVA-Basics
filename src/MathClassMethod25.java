
//math.abs (absolute value) tells you how far away from zero it is 
public class MathClassMethod25 {
	public static void main (String[] args) {
		System.out.println(Math.abs(-26.7)); // absolute value
		System.out.println(Math.ceil(7.4)); // ceiling i.e. round up
		System.out.println(Math.max(45, 2.3));// mpicks the max value
		System.out.println(Math.min(3.2, 8.5)); // picks the min value 
		System.out.println(Math.pow(5, 9));// x to power of y
		System.out.println(Math.sqrt(20));//square root
	} 
}
